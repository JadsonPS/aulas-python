"""
    Crie um algoritmo que leia um número e mostre o seu dobro,
    triplo e raiz quadrada.
"""

numero = float(input("Digite um valor: "))

dobro = numero * 2
triplo = numero * 3
raizQuadr = numero ** (1/2)

print(f'O dobro de {numero } é {dobro} \nO triplo é {triplo} \nA raiz quadrada é {raizQuadr}')
